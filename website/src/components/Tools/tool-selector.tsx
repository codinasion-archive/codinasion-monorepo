export default function ToolSelector({
  slug,
  props,
}: {
  slug: string;
  props: ToolPropsType;
}): JSX.Element {
  const ToolComponent =
    toolComponents[KebabCaseStringToPascalCaseStringFn(slug)];

  if (!ToolComponent) {
    return <></>;
  }

  return <ToolComponent {...props} />;
}
